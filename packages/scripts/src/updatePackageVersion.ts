#!/usr/bin/env node

import { readFileSync, writeFileSync } from "fs";
import { join } from "path";

// Read package.json from mongodb-mcp-server package
const packageJsonPath = join(import.meta.dirname, "../../..", "packages", "mongodb-mcp-server", "package.json");
const packageJson = JSON.parse(readFileSync(packageJsonPath, "utf-8")) as {
    version: string;
};

// Read root package.json for engines field
const rootPackageJsonPath = join(import.meta.dirname, "../../..", "package.json");
const rootPackageJson = JSON.parse(readFileSync(rootPackageJsonPath, "utf-8")) as {
    engines?: { node?: string };
};
const nodeEngine = rootPackageJson.engines?.node ?? ">=20.0.0";

// Define the packageInfo.ts content
const packageInfoContent = `// This file was generated by packages/scripts/src/updatePackageVersion.ts - Do not edit it manually.
export const packageInfo: {
    version: string;
    mcpServerName: string;
    engines: { node: string };
} = {
    version: "${packageJson.version}",
    mcpServerName: "MongoDB MCP Server",
    engines: {
        node: "${nodeEngine}",
    },
};
`;

// Write to packageInfo.ts (in packages/mongodb-mcp-server/src/common/)
const packageInfoPath = join(
    import.meta.dirname,
    "../../..",
    "packages",
    "mongodb-mcp-server",
    "src",
    "common",
    "packageInfo.ts"
);
writeFileSync(packageInfoPath, packageInfoContent);

// Keep the .mcpb manifest's version in lockstep with package.json.
const manifestPath = join(
    import.meta.dirname,
    "../../..",
    "packages",
    "mongodb-mcp-server",
    "packaging",
    "mcpb",
    "manifest.json"
);
const manifest = JSON.parse(readFileSync(manifestPath, "utf-8")) as { version: string };
manifest.version = packageJson.version;
writeFileSync(manifestPath, JSON.stringify(manifest, null, 2) + "\n");

// Keep the Azure Bicep template's default container image version in lockstep with package.json.
const bicepPath = join(
    import.meta.dirname,
    "../../..",
    "packages",
    "mongodb-mcp-server",
    "deploy",
    "azure",
    "bicep",
    "main.bicep"
);
const bicepContent = readFileSync(bicepPath, "utf-8");
if (!packageJson.version.includes("-prerelease.")) {
    const updatedBicepContent = bicepContent.replace(
        /^(param containerImage string = 'mongodb\/mongodb-mcp-server:)[^']+(')/m,
        `$1${packageJson.version}$2`
    );
    writeFileSync(bicepPath, updatedBicepContent);
}
